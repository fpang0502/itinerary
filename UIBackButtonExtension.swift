import UIKit

extension UIButton {
    func createBackButton(){
        backgroundColor = Theme.text
        layer.cornerRadius = frame.height / 2
        layer.shadowOpacity = 0.25
        layer.shadowRadius = 5
        layer.shadowOffset = CGSize(width: 0, height: 10)
    }
}
